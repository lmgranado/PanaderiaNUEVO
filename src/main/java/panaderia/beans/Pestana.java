package panaderia.beans;

public class Pestana {
	
	private String action;
	private String txt;
	private String javascript;
	private String padre;
	private String hijo;
	private String marcado;
	private String completo;
	private String tifo;
	private String subsana;
	private String subsanaTxt;
	
	public Pestana(){}
	
	public Pestana(String action, String txt, String javascript,String padre, String hijo, String marcado, String completo, String tifo){
		this.action=action;
		this.txt=txt;
		this.javascript = javascript;
		this.padre = padre;
		this.hijo = hijo;
		this.marcado = marcado;
		this.completo = completo;
		this.tifo = tifo;
	}
	
	public Pestana(String action, String txt, String javascript,String padre, String hijo, String marcado){
		this.action=action;
		this.txt=txt;
		this.javascript = javascript;
		this.padre = padre;
		this.hijo = hijo;
		this.marcado = marcado;
	}
	
	public Pestana(String action, String txt, String javascript,String padre, String hijo, String marcado, String subsanaTxt){
		this.action=action;
		this.txt=txt;
		this.javascript = javascript;
		this.padre = padre;
		this.hijo = hijo;
		this.marcado = marcado;
		this.subsanaTxt = subsanaTxt;
	}
	
	public String getJavascript() {
		return javascript;
	}
	public void setJavascript(String javascript) {
		this.javascript = javascript;
	}
	public String getTxt() {
		return txt;
	}
	public void setTxt(String txt) {
		this.txt = txt;
	}
	public String getAction() {
		return action;
	}
	public void setAction(String action) {
		this.action = action;
	}

	public String getHijo() {
		return hijo;
	}

	public void setHijo(String hijo) {
		this.hijo = hijo;
	}

	public String getMarcado() {
		return marcado;
	}

	public void setMarcado(String marcado) {
		this.marcado = marcado;
	}

	public String getPadre() {
		return padre;
	}

	public void setPadre(String padre) {
		this.padre = padre;
	}

	public String getCompleto() {
		return completo;
	}

	public void setCompleto(String completo) {
		this.completo = completo;
	}
	
	public String getTifo() {
		return tifo;
	}
	
	public void setTifo(String tifo) {
		this.tifo = tifo;
	}
	
	public String getSubsana() {
		return subsana;
	}
	
	public void setSubsana(String subsana) {
		this.subsana = subsana;
	}
	
	public String getSubsanaTxt() {
		return subsanaTxt;
	}
	
	public void setSubsanaTxt(String subsanaTxt) {
		this.subsanaTxt = subsanaTxt;
	}
}
